package me.mitul.aij.Bean;

public class BeanAllClosingList {
    private String closingBranchName;
    private int closingTfwsValue;
    private int closingOpenValue;
    private int closingEbcValue;
    private int closingSebcValue;
    private int closingScValue;
    private int closingStValue;

    public BeanAllClosingList() {
    }

    public String getClosingBranchName() {
        return closingBranchName;
    }

    public void setClosingBranchName(String p) {
        closingBranchName = p;
    }

    public int getClosingTfwsValue() {
        return closingTfwsValue;
    }

    public void setClosingTfwsValue(int p) {
        closingTfwsValue = p;
    }

    public int getClosingOpenValue() {
        return closingOpenValue;
    }

    public void setClosingOpenValue(int p) {
        closingOpenValue = p;
    }

    public int getClosingEbcValue() {
        return closingEbcValue;
    }

    public void setClosingEbcValue(int p) {
        closingEbcValue = p;
    }

    public int getClosingSebcValue() {
        return closingSebcValue;
    }

    public void setClosingSebcValue(int p) {
        closingSebcValue = p;
    }

    public int getClosingScValue() {
        return closingScValue;
    }

    public void setClosingScValue(int p) {
        closingScValue = p;
    }

    public int getClosingStValue() {
        return closingStValue;
    }

    public void setClosingStValue(int p) {
        closingStValue = p;
    }
}
