package me.mitul.aij.Bean;

public class BeanCommon {
    private int commonID;
    private String commonName;
    private String commonAddress;
    private String commonCity;

    public BeanCommon() {
    }

    public int getCommonID() {
        return commonID;
    }

    public void setCommonID(int p) {
        commonID = p;
    }

    public String getCommonName() {
        return commonName;
    }

    public void setCommonName(String p) {
        commonName = p;
    }

    public String getCommonAddress() {
        return commonAddress;
    }

    public void setCommonAddress(String p) {
        commonAddress = p;
    }

    public String getCommonCity() {
        return commonCity;
    }

    public void setCommonCity(String p) {
        commonCity = p;
    }
}
