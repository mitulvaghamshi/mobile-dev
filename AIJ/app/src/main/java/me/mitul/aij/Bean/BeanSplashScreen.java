package me.mitul.aij.Bean;

public class BeanSplashScreen {
    private String splashText;
    private String splashTextSize;
    private String splashTextColor;

    public String getSplashText() {
        return splashText;
    }

    public void setSplashText(String p) {
        this.splashText = p;
    }

    public String getSplashTextSize() {
        return splashTextSize;
    }

    public void setSplashTextSize(String p) {
        this.splashTextSize = p;
    }

    public String getSplashTextColor() {
        return splashTextColor;
    }

    public void setSplashTextColor(String p) {
        this.splashTextColor = p;
    }
}
